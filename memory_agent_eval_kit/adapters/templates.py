"""Adapter template generation utilities."""

from __future__ import annotations

import re
import textwrap
from dataclasses import dataclass
from pathlib import Path


@dataclass(frozen=True)
class AdapterTemplateResult:
    """Generated adapter template file paths."""

    adapter_path: Path
    test_path: Path
    docs_path: Path


def create_adapter_template(
    name: str, output_dir: str | Path = ".", *, force: bool = False
) -> AdapterTemplateResult:
    """Generate an adapter skeleton, tests, and docs for a custom adapter."""

    module_name = _module_name(name)
    class_name = _class_name(module_name)
    root = Path(output_dir)
    adapter_path = root / "memory_agent_eval_kit" / "adapters" / f"{module_name}.py"
    test_path = root / "tests" / f"test_{module_name}_adapter.py"
    docs_path = root / "docs" / "adapters" / f"{module_name}.md"
    _write(adapter_path, _adapter_source(class_name), force=force)
    _write(test_path, _test_source(module_name, class_name), force=force)
    _write(docs_path, _docs_source(module_name, class_name), force=force)
    return AdapterTemplateResult(
        adapter_path=adapter_path, test_path=test_path, docs_path=docs_path
    )


def _write(path: Path, content: str, *, force: bool) -> None:
    if path.exists() and not force:
        raise FileExistsError(f"Refusing to overwrite existing file: {path}")
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")


def _module_name(name: str) -> str:
    normalized = re.sub(r"[^a-zA-Z0-9_]+", "_", name.strip()).strip("_").lower()
    if not normalized or not re.match(r"^[a-zA-Z_]", normalized):
        raise ValueError("Adapter name must start with a letter or underscore")
    return normalized


def _class_name(module_name: str) -> str:
    return "".join(part.capitalize() for part in module_name.split("_")) + "Adapter"


def _adapter_source(class_name: str) -> str:
    return textwrap.dedent(
        f'''
        """Custom memory adapter generated by memory-eval."""

        from __future__ import annotations

        from memory_agent_eval_kit.adapters.base import MemoryAgentAdapter, MemoryRecord


        class {class_name}(MemoryAgentAdapter):
            """Local custom adapter skeleton with an in-memory smoke-test implementation."""

            def __init__(self) -> None:
                self._memories: dict[str, str] = {{}}

            def query(self, prompt: str) -> str:
                words = {{word.lower().strip("?:,.") for word in prompt.split()}}
                matches = [
                    content
                    for content in self._memories.values()
                    if words & {{word.lower().strip("?:,.") for word in content.split()}}
                ]
                return "\\n".join(matches) if matches else "I do not know."

            def add_memory(self, memory: MemoryRecord) -> None:
                memory_id = str(memory.get("memory_id", len(self._memories) + 1))
                self._memories[memory_id] = str(memory.get("content", ""))

            def delete_memory(self, memory_id: str) -> None:
                self._memories.pop(memory_id, None)
        '''
    ).lstrip()


def _test_source(module_name: str, class_name: str) -> str:
    return textwrap.dedent(
        f"""
        from __future__ import annotations

        from memory_agent_eval_kit.adapters.{module_name} import {class_name}


        def test_{module_name}_adapter_smoke() -> None:
            adapter = {class_name}()
            adapter.add_memory(
                {{"memory_id": "custom", "content": "Custom adapter fact is green"}}
            )
            assert "green" in adapter.query("What is the custom adapter fact?")
            adapter.delete_memory("custom")
            assert "I do not know" in adapter.query("What is the custom adapter fact?")
        """
    ).lstrip()


def _docs_source(module_name: str, class_name: str) -> str:
    return textwrap.dedent(
        f"""
        # {class_name}

        Generated adapter files:

        - `memory_agent_eval_kit/adapters/{module_name}.py`
        - `tests/test_{module_name}_adapter.py`

        The generated class implements the `MemoryAgentAdapter` contract and includes a
        local smoke-test implementation. Customize the methods to connect your memory
        system, then run:

        ```bash
        ruff check .
        mypy memory_agent_eval_kit
        pytest
        ```
        """
    ).lstrip()
